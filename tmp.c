void	sizing_map(t_cub *cub)
{
    int		i;
    char	**map;

	map = cub->map;
	cub->width_map = 0;
    i = 0;
    while (map[i])
	{
		if (cub->width_map < (int)ft_strlen(map[i]))
			cub->width_map = ft_strlen(map[i]);
		i++;
	}
	cub->height_map = i;
}


